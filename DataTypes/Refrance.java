// also known as reference data types

public class Refrance {
    public static void main(String[] args) {
        String name = "Veer";
        int age = 20;
        Double height = 5.9;

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
      
    }
     
}
