

public class Relational {
    public static void main(String[] args) {
        
        int x=20;
        int y=10;
        int a=2;
        int b= 5;


     /*
     Relational Operator
    
        >
        <
        ==
        !=
        <=
        >=
    */

       
        System.out.println("X = 20  Y=10  A=2  B=5");

        if (x>y) {
            System.out.println("X is greater then Y");
        }
        if (x>=y) {
            System.out.println("X is greater then equal to Y");
            
        }
        if (a<b) {
            System.out.println("A is less then B");
            
        }
        if (a<=b) {
            System.out.println("A is less then equal to B");
            
        }
        

    }
    
}
