
public class IncDecOperator {
    public static void main(String[] args) {

          /*
     Increment and Decrement Operator
    
          ++a
          a++

          --a
          a--
    */

          int a=10;
          
          ++a; // increment then use (current value = 11)
          System.out.println(a);
          --a; // decrement then use (current value = 10)
          System.out.println(a);
          a++; // use then increment (current value = 11)
          System.out.println(a);
          a--; // use then decrement (current value = 10)
          System.out.println(a);




    }
    
}
